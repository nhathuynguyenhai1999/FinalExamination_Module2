import java.util.ArrayList;

public class Product {
    String code;
    String name;
    double price;
    int quantity;
    String description;
    Product(String code, String name, double price, int quantity, String description){
        this.code = code;
        this.name = name;
        this.price = price;
        this.quantity = quantity;
        this.description = description;
    }
}
